console.log('Operators in Js')

console.log('Example for Arithmatic Operators ');
let a1 = 5;
let b1 = 4;
console.log('a1 ** b1 = ' + a1 ** b1) //This is an example for Exponetial Operators

console.log('Example for Assignment Operators')
let a2 = 3, b2 = 4;
console.log('a2+=2 =', a2 += 2) //Same as a2=a2+2;
console.log('a2**=b2 =', a2 **= b2) //Exponential with assignment Operators Same as a2=a2**b2
console.log('a2 = ', a2, 'b2 = ', b2)


console.log('Comparison Operators');
console.log(7 == '7') //Matches value only --true
console.log(7 === '7') //Matches Equal value and Type --false
console.log(7 !== '7')//Matches not of (Equal value and Type) --true
console.log('Arunesh' !== 'Arunesh') // --false

console.log(7 != 7) //--false
console.log(7 != '7') //--false
console.log(7 !== 7) //--false
console.log(7 !== '7') //--true

console.log(5 >= 5) // '>=' is used for comparison


console.log('Logical Operators in Js')
let x = 5, y = 6
console.log(x < y && x == 5) //and Operators 
console.log(x > y || x == 5) //OR Operators 
console.log(!(x == 5)) //not Operators 
